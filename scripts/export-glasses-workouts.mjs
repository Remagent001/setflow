// Exports Keith's real workout plans from Supabase into a source file that the
// glasses build bakes in (offline-safe; no auth needed on the lens for now).
// Re-run after editing workouts on the web:  node scripts/export-glasses-workouts.mjs
import { createClient } from "@supabase/supabase-js";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const KEITH_ID = "ac5fb54d-08bb-4c08-be85-5fd0dc0e277a";
const root = dirname(dirname(fileURLToPath(import.meta.url))); // setflow/
const env = Object.fromEntries(
  readFileSync(join(root, ".env"), "utf8").split("\n")
    .filter((l) => l.includes("=") && !l.trim().startsWith("#"))
    .map((l) => [l.slice(0, l.indexOf("=")).trim(), l.slice(l.indexOf("=") + 1).trim()])
);
const sb = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.SUPABASE_SERVICE_ROLE_KEY, {
  auth: { autoRefreshToken: false, persistSession: false },
});

const TS = "2026-07-06T00:00:00.000Z";

// Pull the "Day N ·" plans in order.
const { data: plans, error: pErr } = await sb
  .from("workout_plans")
  .select("id, title, description, estimated_duration_minutes")
  .eq("owner_user_id", KEITH_ID)
  .like("title", "Day %")
  .order("title");
if (pErr) throw pErr;
if (!plans?.length) throw new Error("no Day plans found for Keith");

const workouts = [];
for (const p of plans) {
  const { data: steps, error: sErr } = await sb
    .from("workout_steps")
    .select("id, exercise_id, order_index, set_count, target_reps, target_weight, target_duration_seconds, rest_seconds, cue")
    .eq("workout_plan_id", p.id)
    .order("order_index");
  if (sErr) throw sErr;

  const exIds = [...new Set(steps.map((s) => s.exercise_id))];
  const { data: exs, error: eErr } = await sb
    .from("exercises")
    .select("id, name, cues, primary_muscle_group, common_mistakes")
    .in("id", exIds);
  if (eErr) throw eErr;
  const exById = new Map(exs.map((e) => [e.id, e]));

  workouts.push({
    plan: {
      id: p.id, ownerUserId: KEITH_ID, title: p.title,
      description: p.description ?? undefined,
      estimatedDurationMinutes: p.estimated_duration_minutes ?? undefined,
      createdAt: TS, updatedAt: TS,
    },
    steps: steps.map((s) => {
      const e = exById.get(s.exercise_id);
      return {
        step: {
          id: s.id, workoutPlanId: p.id, exerciseId: s.exercise_id, orderIndex: s.order_index,
          setCount: s.set_count, targetReps: s.target_reps ?? undefined,
          targetWeight: s.target_weight ?? undefined,
          targetDurationSeconds: s.target_duration_seconds ?? undefined,
          restSeconds: s.rest_seconds, cue: s.cue ?? undefined,
        },
        exercise: {
          id: e?.id ?? s.exercise_id, name: e?.name ?? "Exercise",
          primaryMuscleGroup: e?.primary_muscle_group ?? undefined,
          cues: e?.cues ?? [], commonMistakes: e?.common_mistakes ?? [],
          createdAt: TS, updatedAt: TS,
        },
      };
    }),
  });
}

const header =
  "// AUTO-GENERATED by scripts/export-glasses-workouts.mjs — do not edit by hand.\n" +
  "// Keith's real workout plans, baked into the glasses build (offline-safe).\n" +
  "// Re-run the script after editing workouts on the web, then rebuild the glasses app.\n\n" +
  "import type { EngineWorkout } from \"../../../packages/workout-engine/src/engine\";\n\n";
const body = "export const WORKOUTS: EngineWorkout[] = " + JSON.stringify(workouts, null, 2) + ";\n";

const outPath = join(root, "apps", "glasses", "src", "workouts.generated.ts");
writeFileSync(outPath, header + body);
console.log(`wrote ${workouts.length} workouts -> apps/glasses/src/workouts.generated.ts`);
for (const w of workouts) console.log(`  ${w.plan.title}: ${w.steps.length} exercises`);
